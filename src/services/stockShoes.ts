// Helper function to get shoe by ID or UID
export const getShoeById = (id: string): StockShoes | undefined => {
  return mockStockShoes.find(
    (shoe) => shoe.uid === id || shoe.id.toString() === id
  );
};

// Helper function to get shoes by category
export const getShoesByCategory = (category: string): StockShoes[] => {
  return mockStockShoes.filter(
    (shoe) =>
      shoe.category.toLowerCase() === category.toLowerCase() &&
      Number(shoe.stock) > 0
  );
};

